import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IMedicalDeclaration } from '@/shared/model/medical-declaration.model';

const baseApiUrl = 'api/medical-declarations';

export default class MedicalDeclarationService {
  public find(id: string): Promise<IMedicalDeclaration> {
    return new Promise<IMedicalDeclaration>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IMedicalDeclaration): Promise<IMedicalDeclaration> {
    return new Promise<IMedicalDeclaration>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IMedicalDeclaration): Promise<IMedicalDeclaration> {
    return new Promise<IMedicalDeclaration>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IMedicalDeclaration): Promise<IMedicalDeclaration> {
    return new Promise<IMedicalDeclaration>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
